package ex6_ifstatements;

public class IfElseIfStatement {

    public static void main(String[] args) {
        int i = 60;
        int j = 50;

        if (i>70){
            System.out.println("I is Big Number");
        }else if (j>70){
            System.out.println("J is Big Number");
        }else {
            System.out.println(" 70 is Big Number");
        }
    }
}
