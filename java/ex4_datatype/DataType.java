package ex4_datatype;

public class DataType {

    public static void main(String[] args) {

//    Integer Family
//    Byte range is -128 to 127
        byte a =-127;
        System.out.println(a);

//    Short range is -32768 to 32767
        short b= 32767;
        System.out.println(b);

//    Int rang is -2,147,483,648 to 2,147,483,647
        int c = 123456;
        System.out.println(c);

//    Long rang is -9,223,372,036,854,778,808 to 9,223,372,036,854,775,807
        long d = 9223372036854775807L;
        System.out.println(d);

//     Float Family
//     Float rang is 3.4E-38 to 3.4E+38
        float e = 15000.666666666666666666666666666666666666666666666666666666666666666666666666666666666666666666666666666666f;
        System.out.println(e);

//     double  rang is approximately 1.8 *(10)308
        double f = 15000.99999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999999;
        System.out.println(f);

//      Character Family
//      Character range is unicode value (  Single range value )
        char g = 'T';
        System.out.println(g);


//      Boolean Family
//      Boolean don't have any range, It's a fix value True or False
        boolean h = true;
        System.out.println(h);

//      String Family
//      String range is 0 to 2,147,483,647
        String i = "I am a Tester";
        System.out.println(i);

//  String Concatenation
        String  j = "Software";
        String  k = "Testing";
        System.out.println(j+" "+k);







    }



}
