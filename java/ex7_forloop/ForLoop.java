package ex7_forloop;

public class ForLoop {

    public static void main(String[] args) {
        for (int i=0;i<=5;i--){
            System.out.println("i:" +i);
        }
    }
}
/*
i=0  0<=5  i: 0   0+1

i=1  1<=5  i: 1   1+1

i=2  2<=5  i: 2   2+1

i=3  3<=5  i: 3   3+1

i=4  4<=5  i: 4   4+1

i=5  5<=5  i: 5   5+1

i=6  6<=5









 */