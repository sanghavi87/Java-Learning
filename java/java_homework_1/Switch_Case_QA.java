package java_homework_1;

public class Switch_Case_QA {
    public static void main(String[] args) {

 //QA-1. Write a program to print a specific month in the calendar
        //for a given value.

  int Month=18;                // to get the different result - change the number here

  switch (Month)
  {
      case 1: System.out.println("1st Month of the Year: January"); break;
      case 2: System.out.println("2nd Month of the Year: February"); break;
      case 3: System.out.println("3rd Month of the Year: March"); break;
      case 4: System.out.println("4th Month of the Year: April"); break;
      case 5: System.out.println("5th Month of the Year: May"); break;
      case 6: System.out.println("6th Month of the Year: June"); break;
      case 7: System.out.println("7th Month of the Year: July"); break;
      case 8: System.out.println("8th Month of the Year: August"); break;
      case 9: System.out.println("9th Month of the Year: September"); break;
      case 10: System.out.println("10th Month of the Year: October"); break;
      case 11: System.out.println("11th Month of the Year: November"); break;
      case 12: System.out.println("12th Month of the Year: December"); break;
      default: System.out.println("Invalid data input to select the Month");


  }











    }
}
