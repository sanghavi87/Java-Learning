package java_homework_1;

public class For_Loop_QA {
    public static void main(String[] args) {


//QA-1. Write a program to print 10 even numbers and 10 odd numbers.


  for(int i=0; i<=20; i+=2)                  //Even no. example
  {
      System.out.println("Print Even Number: "+(i));
  }


  for(int i=1; i<=20; i+=2)                 //Odd no. example
  {
      System.out.println("Print Odd Number: "+(i));
  }


  //--------------------------------------------------------------------------------------------------------------------------


  //QA-2. Write a program to generate tables of 10.


  for(int i=1; i<=10;  i++)
  {
      System.out.println("10 * " + (i) + " = " + (10 * i));
  }














    }
}
