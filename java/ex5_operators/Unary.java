package ex5_operators;

public class Unary {

    public static void main(String[] args) {
        int i =10;
        boolean j=true;

        System.out.println(i++);  // 10  10+1  i =11
        System.out.println(++i);  // 11+1 i=12 12
        System.out.println(i--);  // 12 12-1 i=11
        System.out.println(--i); // 11-1 i=10 10
        System.out.println(!j);  //


    }

}
