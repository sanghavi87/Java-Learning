package ex5_operators;

public class Relational {
    public static void main(String[] args) {
        int i=35;
        int j=36;

        System.out.println(i==j); //false
        System.out.println(i!=j); //true
        System.out.println(i>j);  //false
        System.out.println(i<j);  //true
        System.out.println(i>=j); //false
        System.out.println(i<=j); //true

    }
}
